import { useEffect } from "react";
import { useLocation } from "react-router-dom";

export default function ScrollToTop() {
  const { pathname } = useLocation();

  useEffect(() => {
    window.scrollTo({
      top: 0,
      left: 0,
      behavior: "instant", // Used instant instead of smooth to prevent jarring visual scroll on route pop
    });
  }, [pathname]);

  return null;
}
